#include <stdio.h>
/**
 * add - returns the sum
 * @a: first integer
 * @b: second integer
 * Return: returns the sum
*/
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - returns the sub
 * @a: first integer
 * @b: second integer
 * Return: returns the sub
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - returns the mul
 * @a: first integer
 * @b: second integer
 * Return: returns the mul
*/
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - returns the div
 * @a: first integer
 * @b: second integer
 * Return: returns the div
*/
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns the mod
 * @a: first integer
 * @b: second integer
 * Return: returns themod
*/
int mod(int a, int b)
{
	return (a % b);
}
